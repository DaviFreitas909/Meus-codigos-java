// ADIÇÃO
console.log(adicao(11, 11));
function adicao(num1, num2) {
    return (num1 + num2)
}

// SUBTRAÇÃO
console.log(subtracao(12, 6));
function subtracao(num1, num2) {
    return (num1 - num2)
}

// DIVISAO 
console.log(divisao(12, 3));
function divisao(num1, num2) {
    return (num1 / num2)
}

// MUTIPLICAÇAO
console.log(multiplicacao(45, 54));
function multiplicacao(num1, num2) {
    return (num1 * num2)
}

